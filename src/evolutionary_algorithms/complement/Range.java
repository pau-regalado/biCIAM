package evolutionary_algorithms.complement;

public class Range {
   private Probability data;
   private float max;
   private float min;
   
   public Probability getData() {
	  return data;
   }
   public void setData(Probability data) {
	  this.data = data;
   }
   public float getMax() {
	  return max;
   }
   public void setMax(float max) {
	  this.max = max;
   }
   public float getMin() {
	  return min;
   }
   public void setMin(float min) {
	  this.min = min;
   }
}
